package dst2.server.utils.exceptions.jobmanagement;

public class NoGridFoundException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -898315928297979053L;

	public NoGridFoundException() {
		super();
	}

	public NoGridFoundException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NoGridFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoGridFoundException(String message) {
		super(message);
	}

	public NoGridFoundException(Throwable cause) {
		super(cause);
	}
	
	
}
